<p>It's not that the distance has changed, it is the immediacy and availability. When I was in the US, I was always available. You could pick up a phone and call me at any time. Or you could email me and I would get it within a number of hours. And even if we didn't live in the same city, there was a chance I could come by. Now that I'm overseas, all of that has changed. Communication is less reliable, and the time of response jumps. And that chance of a visit is no more. That's what has changed.
