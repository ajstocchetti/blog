<p>In Chiang Mai, you can buy clean, potable water on the street. Every couple of blocks, there are stations where you insert a coin and can fill up your water bottle. I'm glad that the city (or it could be a national initiative) is promoting clean water. But I've only seen tourists use these, so maybe they were up so tourists don't complain about drinking water. Regardless, it is nice that there are outlets to refill a container, instead of buying a new water bottle every time.
</p>

