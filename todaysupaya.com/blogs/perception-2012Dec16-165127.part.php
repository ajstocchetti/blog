<i>This conversation never happened. It was inspired by a misunderstanding I had one morning waking up next to my girlfriend...</i>
<br /><br />
<b>ajs: </b>It's just...the more I think about it, the more skeptical I become. How can we trust our eyes? How can we trust any of our senses?
<br /><br />
<b>msg: </b>What do you mean?
<br /><br />
<b>ajs: </b>Well, things are. They exist as they exist. But that's not what we see. We see what the given light conditions and physical setting allow us to see. And we perceive something totally different. What we perceive is conditioned by our past experiences.
<br /><br />
<b>msg: </b>How so?
<br /><br />
<b>ajs: </b>Learning off of old learning - you see, as soon as we start getting a sense of space and understanding the way three-dimensions work, our brain starts to base its "understanding" on both the data it is currently getting from the eyes, AND from the memories it has. Our ability to learn/understand is biased by what we already know.
<br /><br />
<b>msg: </b>So you're saying that we can't truly experience the world as it is because our thoughts are compromised by what we have already seen?
<br /><br />
<b>ajs: </b>Or heard, or felt, or smelled or tasted. And most importantly, something we've already thought. And any combination of many of those.
<br /><br />
<b>msg: </b>So what I perceive isn't actually the real world, it's the real world modified by my brain.
<br /><br />
<b>ajs: </b>Precisely.
<br /><br />
<b>msg: </b>And the more we live and experience, our perceptions are influenced more by our brain than by sensual inputs.
<br /><br />
<b>ajs: </b>Yes, the more we live the less we actually experience. We are continuously assimilating a world in our head that is how we think the world should be, based on how we have noticed the world is.  We then live in this world in our heads and not the world that is around us. We are so conditioned by past experiences we can no longer look at things with an open mind.
<br /><br />
<b>msg: </b>But when I look at the world around me, I don't think about it, I just experience it.
<br /><br />
<b>ajs: </b>No, the brain conditions what you see so fast you don't even realize it has happened.
<br /><br />
<b>msg: </b>That's ridiculous.
<br /><br />
<b>ajs: </b>No it's not. It's why MC Escher was so popular. He was able to manipulate the brain's preconceived notions and put opposing conditions in the same space.
<br /><br />
<b>msg: </b>Okay, maybe you're right...maybe our past experiences influence what we experience in the future. But what's the big deal? Why does it matter? Doesn't this conditioning just allow us to experience the world faster? Or allow us to figure things out when not all the information is present?
<br /><br />
<b>ajs: </b>Yes, it does give us these capabilities, but it also masks the truth. If what we are experiencing is not the world, but the world after our brain processes and modifies it, then we don't actually live in the world. We live in our heads! I for one don't want that. I want to experience the world as it actually is. And to do that, one needs to question everything. You can only trust your senses once. After that, your brain lies to you.
<br /><br />
<b>msg: </b>Give me a break. I can't imagine a real situation - not some made up MC Escher drawing - where I don't have enough intellect to figure out what it actually going on.
<br /><br />
<b>ajs: </b>I hope for your sake you're right.
